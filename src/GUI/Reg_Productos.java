    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        float precio;
        int categoria;
        if((txfNombre.getText().isEmpty()
                ||txfPreio.getText().isEmpty()
                ||txfProveedor.getText().isEmpty()))
        {
            Utiles.errCmpsVacios("Nombre,Precio,Proveedor");
        }
        else try
        {
            if(Utiles.chckNumber(txfPreio.getText(), Utiles.TO_FLOAT))
            {
                precio = Float.parseFloat(txfPreio.getText());
                if(cmbCategoria.getSelectedIndex()!=0)
                {
                    categoria=cmbCategoria.getSelectedIndex()-1;
                    JOptionPane.showMessageDialog(rootPane, catnm.elementAt(categoria)
                            +"\n"+catid.elementAt(categoria));
                    //registro
                }
                else
                {
                    Utiles.errCombo("Categoria");
                }
            }
            else
            {
                Utiles.errNumParse("Precio");       
            }
        }
        catch(Exception a)
        {
                    
        }
    }//GEN-LAST:event_jButton2ActionPerformed

